package p0205Programa5tik14raDowhile;

public class p0205Programa5tik14raDowhile {

    public static void main(String[] args) {
        
        int zenbakia = 5;
        
        // 5-14 arteko zenbakiak inprimatuko ditu
        do {
            System.out.println("Zenbakia: " + zenbakia);
            zenbakia++;
        } while (zenbakia >= 5 && zenbakia <= 14);

    }	

}
